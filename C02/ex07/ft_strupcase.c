char	*ft_strupcase(char *str)
{
	char	*p;

	p = str;
	while (*p != '\0')
	{
		if (*p > 96 && *p < 123)
		{
			*p = *p - 32;
			p++;
		}
		else
			p++;
	}
	return (str);
}
