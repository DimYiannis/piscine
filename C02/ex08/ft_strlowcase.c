char	*ft_strlowcase(char *str)
{
	char	*p;

	p = str;
	while (*p != '\0')
	{
		if (*p > 64 && *p < 91)
		{
			*p = *p + 32;
			p++;
		}
		else
			p++;
	}
	return (str);
}
